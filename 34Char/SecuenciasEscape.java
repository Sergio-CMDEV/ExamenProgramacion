public class SecuenciasEscape {
    public static void main(String[] args) {
        /* Ejemplo de secuencias de escape en Java */
        System.out.println("\tTabulación");
        System.out.println("\nSalto de línea");
        System.out.println("\"Comillas dobles\"");
        System.out.println("\\Barra invertida\\");
        System.out.println("\'Comilla simple\'");
        System.out.println("\rRetorno de carro");
        System.out.println("\bRetroceso");
        System.out.println("\fAvance de página");
    }
}
